function changeLang(lang) {
    const t = translations[lang] || translations['en'];
    const elements = {
        'main-subtitle': 'subtitle',
        'section-title': 'secTitle',
        'section-desc': 'secDesc',
        'radius-text': 'radius',
        'card1-title': 'c1t',
        'card1-desc': 'c1d',
        'card2-title': 'c2t',
        'card2-desc': 'c2d',
        'cta-title': 'ctaT',
        'cta-btn': 'ctaB',
        'email-placeholder': 'mailPH',
        'submit-text': 'mailSub',
        'nav-home': 'navHome',
        'nav-info': 'navInfo',
        'nav-qa': 'navQA',
        'qa-title': 'qaTitle',
        'qa-1-q': 'qa1q',
        'qa-1-a': 'qa1a',
        'qa-2-q': 'qa2q',
        'qa-2-a': 'qa2a',
        'qa-form-title': 'qaFormTitle',
        'qa-form-placeholder': 'qaFormPlaceholder',
        'qa-form-submit': 'qaFormSubmit',
    };

    for (const id in elements) {
        const element = document.getElementById(id);
        if (element) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = t[elements[id]];
            } else {
                element.innerText = t[elements[id]];
            }
        }
    }
    localStorage.setItem('lang', lang);
}


// Dark Mode Toggle
const themeSwitch = document.querySelector('#checkbox');

themeSwitch.addEventListener('change', () => {
  document.body.classList.toggle('dark-mode');
  if (document.body.classList.contains('dark-mode')) {
    localStorage.setItem('theme', 'dark');
  } else {
    localStorage.setItem('theme', 'light');
  }
});

// On page load
const currentTheme = localStorage.getItem('theme');
if (currentTheme) {
    document.body.classList.add(currentTheme + '-mode');
    if (currentTheme === 'dark') {
        themeSwitch.checked = true;
        document.body.classList.add('dark-mode');
    }
}

